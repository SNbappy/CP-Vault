void Depressed_C0der()
{
    int n;
    cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++)
        cin >> a[i];

    unordered_map<int, vector<int>> have, need;

    for (int i = 1; i <= n; i++) {
        int root = i;
        while(root % 2 == 0)
            root /= 2;

        have[root].push_back(a[i]);
        need[root].push_back(i);
    }

    for (auto [x, y]: have){
        auto target = need[x];

        sort(all(y));

        if (target != y) {
            cout << "NO" << '\n';
            return;
        }
    }

    cout << "YES" << "\n";
}

