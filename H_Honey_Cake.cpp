void Depressed_C0der()
{
    int w, h, d, n;
    cin >> w >> h >> d >> n;
    int a = gcd(n, w);
    n /= a;
    int b = gcd(n, h);
    n /= b;
    int c = gcd(n, d);
    n /= c;
    if (n == 1)
        cout << a - 1 << " " << b - 1 << " " << c - 1;
    else
        cout << -1 << "\n";
}

